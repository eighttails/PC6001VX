  for (i = 0x2120; i < NCHR; i++) {
    if (charset == UNI && put_jisx0213) {
      for (j = 0; j < sizeof(combine) / sizeof(combine[0][0]) / 2; j++) {
	if (i == combine[j][0]) {
	  int k;
	  for (k = 0; k < 16; k++) {
	    bmp60_left[i][k] =
	      bmp60_left[combine[j][1]][k] | bmp60_left[0x309a][k];
	    bmp60_right[i][k] =
	      bmp60_right[combine[j][1]][k] | bmp60_right[0x309a][k];
	  }
	}
      }
    }
    for (j = 0; j < sizeof(jisx0213tbl) / sizeof(jisx0213tbl[0]); j++) {
      if (i == jisx0213tbl[j]) {
	break;
      }
    }
    if (!put_jisx0213 && j < sizeof(jisx0213tbl) / sizeof(jisx0213tbl[0])) {
      for (j = 0; j < 16; j++) {
	bmp60_left[i][j] = 0;
	bmp60_right[i][j] = 0;
      }
    }
  }

