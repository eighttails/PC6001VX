	bmp60_left[i + 0x100][j] +=
	  ((data & 0xc0) ? 0x08 : 0x00) +
	  ((data & 0x30) ? 0x04 : 0x00) +
	  ((data & 0x0c) ? 0x02 : 0x00) +
	  ((data & 0x03) ? 0x01 : 0x00);
