	  ((data & 0xc0) ? 0x80 : 0x00) +
	  ((data & 0x30) ? 0x40 : 0x00) +
	  ((data & 0x0c) ? 0x20 : 0x00) +
	  ((data & 0x03) ? 0x10 : 0x00);
